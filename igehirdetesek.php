    <script>
      function showSearch() {
        document.getElementById("searchForm").style.display = "block";
        window.scrollTo({
          top: document.getElementById("searchForm").offsetTop - 100,
          behavior: "smooth",
        });
      }

      function hideSearch(save) {
        if (save) {
          // Keresés végrehajtása
          alert("Keresés végrehajtva");
        }
        document.getElementById("searchForm").style.display = "none";
      }

      // Év és hónap választók interaktivitása
      document.querySelectorAll(".year-btn").forEach((btn) => {
        btn.addEventListener("click", function () {
          document
            .querySelectorAll(".year-btn")
            .forEach((b) => b.classList.remove("active"));
          this.classList.add("active");
          // Itt történne a szűrés az év szerint
        });
      });

      document.querySelectorAll(".month-link").forEach((link) => {
        link.addEventListener("click", function (e) {
          e.preventDefault();
          // Itt történne a görgetés a kiválasztott hónaphoz
          alert("Görgetés a " + this.textContent + " hónaphoz");
        });
      });
